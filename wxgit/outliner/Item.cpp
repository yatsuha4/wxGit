#include "wxgit/outliner/Item.hpp"

namespace wxgit {
namespace outliner {
/***********************************************************************//**
	@brief コンストラクタ
        @param[in] text 表示名
***************************************************************************/
Item::Item(const wxString& text)
  : text_(text), 
    outliner_(nullptr)
{
}
/***********************************************************************//**
	@brief デストラクタ
***************************************************************************/
Item::~Item() {
}
/***********************************************************************//**
	@brief 
***************************************************************************/
void Item::link(Outliner* outliner, const wxTreeListItem& id) {
  wxASSERT(!outliner_);
  outliner_ = outliner;
  id_ = id;
}
/***********************************************************************//**
	@brief 
***************************************************************************/
void Item::unlink() {
  wxASSERT(outliner_);
  outliner_ = nullptr;
}
/***********************************************************************//**
	$Id$
***************************************************************************/
}
}
